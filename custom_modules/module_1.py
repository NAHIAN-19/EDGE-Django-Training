# Custom module to convert temperature to farenheit

# Function to return temperature in farenheit from celsius
def celsiusToFarenHeit(celsius):
    farenheit = (celsius*(9/5)) + 32
    return farenheit

# Calculate Area of circle using defined pi and radius
def calculateArea(radius):
    # Initialize pi 
    pi = 3.14156
    area_of_circle = pi * radius * radius
    
    return area_of_circle